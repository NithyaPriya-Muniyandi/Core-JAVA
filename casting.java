//Type Casting in Java

import java.lang.*;

class casting{
	public static void main(String args[]){
		int a=10;
		double b=a, d=24.5678;
		int c=(int)d;
		System.out.println("Int :" +a);
		System.out.println("Double :" +b);
		System.out.println("Double :" +d);
		System.out.println("Int :" +c);
}}